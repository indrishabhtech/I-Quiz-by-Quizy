function endQuiz() {
    const quizContainer = document.getElementById('quiz-container');
    quizContainer.innerHTML = `<h2>Quiz Completed!</h2>
<p>Your Score: ${score} out of ${quizData.length} </p> <p> Thanks For Completing <br> Wanna try out other Quiz ! </p> <br> <a href="index.html"> Yes </a>`;
}
